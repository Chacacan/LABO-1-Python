# f) Recorrer la lista y determinar la altura promedio de los  superhéroes (PROMEDIO).


def determinar_promedio_de_altura_heroes(lista_heroes: list[dict]) -> dict:
    contador_de_alturas = 0
    suma_alturas = 0
    for heroe in lista_heroes:
        altura = float(heroe.get("altura"))
        contador_de_alturas += 1
        suma_alturas += altura
    promedio_alturas = suma_alturas / contador_de_alturas
    mensaje = f"el promedio de altura de los superheroes es de {promedio_alturas} "
    print(mensaje)
    return promedio_alturas


